const supportedLanguages = ["es", "en", "fr", "pl", "pt", "de", "it", "dk"];
const fallbackLanguage = "es";
const storageKey = "dpia_lang";

function normalizeLanguage(value) {
  if (!value || typeof value !== "string") {
    return fallbackLanguage;
  }

  const base = value.toLowerCase().split("-")[0];
  const normalized = base === "da" ? "dk" : base;

  if (supportedLanguages.includes(normalized)) {
    return normalized;
  }

  return fallbackLanguage;
}

function detectBrowserLanguage() {
  const candidates = Array.isArray(navigator.languages) && navigator.languages.length > 0
    ? navigator.languages
    : [navigator.language];

  for (const candidate of candidates) {
    const normalized = normalizeLanguage(candidate);
    if (supportedLanguages.includes(normalized)) {
      return normalized;
    }
  }

  return fallbackLanguage;
}

function getInitialLanguage() {
  const stored = localStorage.getItem(storageKey);
  if (stored && supportedLanguages.includes(stored)) {
    return stored;
  }

  return detectBrowserLanguage();
}

function applyTranslations(lang) {
  const language = supportedLanguages.includes(lang) ? lang : fallbackLanguage;
  const dictionary = translations[language] || translations[fallbackLanguage];

  document.documentElement.lang = language;

  const textElements = document.querySelectorAll("[data-i18n]");
  textElements.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (!key) return;
    const value = dictionary[key] ?? translations[fallbackLanguage][key];
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  const htmlElements = document.querySelectorAll("[data-i18n-html]");
  htmlElements.forEach((element) => {
    const key = element.getAttribute("data-i18n-html");
    if (!key) return;
    const value = dictionary[key] ?? translations[fallbackLanguage][key];
    if (typeof value === "string") {
      element.innerHTML = value;
    }
  });

  const selector = document.getElementById("language-select");
  if (selector && selector.value !== language) {
    selector.value = language;
  }
}

document.addEventListener("DOMContentLoaded", () => {
  const selector = document.getElementById("language-select");
  const initialLanguage = getInitialLanguage();

  applyTranslations(initialLanguage);

  if (selector) {
    selector.value = initialLanguage;
    selector.addEventListener("change", (event) => {
      const selectedLanguage = normalizeLanguage(event.target.value);
      localStorage.setItem(storageKey, selectedLanguage);
      applyTranslations(selectedLanguage);
    });
  }
});
